CREATE TABLE clientes (
codcliente  	NUMBER(4) PRIMARY KEY,
Dni	    	VARCHAR2(10),
nombre      	VARCHAR2(10), 
apellidos	VARCHAR2(20),
codcuenta    	NUMBER(10) 
);
  
CREATE TABLE cuentas (
codcuenta   NUMBER(10) PRIMARY KEY,
nomcuenta   VARCHAR2(20), 
direccion   VARCHAR2(20),
codsucursal NUMBER(4)
);

CREATE TABLE sucursales (
codsucursal NUMBER(10) PRIMARY KEY,
nomsucursal VARCHAR2(30), 
direccion   VARCHAR2(30)
);

