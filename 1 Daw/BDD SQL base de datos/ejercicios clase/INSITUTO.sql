CREATE TABLE profesores (
codprofesor NUMBER(4),
nomprofesor VARCHAR2(20),
codasig     NUMBER(4) PRIMARY KEY,
numalumnos  NUMBER(5)
);

CREATE TABLE asignaturas (
codasig     NUMBER(4) PRIMARY KEY,
nomasig     VARCHAR2(20), 
curso	    NUMBER(1),
grupo       VARCHAR2(5),
numalumnos  NUMBER(5)
);

CREATE TABLE alumnos (
nombre      VARCHAR2(20),
apellido1   VARCHAR2(30),
apellido2   VARCHAR2(30),
curso       NUMBER(1),
grupo       VARCHAR2(5),
codasig     NUMBER(4),
tutor       VARCHAR2(20),
nota1       NUMBER(2),
nota2       NUMBER(2),
nota3       NUMBER(2)
);

